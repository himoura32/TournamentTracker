        private static string ConvertRoundListToString(List<List<MatchupModel>> rounds)
        {
            string output = "";

            if (rounds.Count == 0)
            {
                return "";
            }

            foreach (List<MatchupModel> m in rounds)
            {
                output += $"{ ConvertMatchupListToString(m) }|";
            }
            output = output.Substring(0, output.Length - 1);

            return output;
        }

